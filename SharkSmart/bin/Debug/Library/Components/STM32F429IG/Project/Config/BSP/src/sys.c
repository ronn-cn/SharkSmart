//关闭所有中断(但是不包括fault和NMI中断)
void INTX_DISABLE(void)
{
	
	__asm__ __volatile__("CPSID   I");
	__asm__ __volatile__("BX      LR");
  
}
//开启所有中断
void INTX_ENABLE(void)
{
	__asm__ __volatile__("CPSIE   I");
	__asm__ __volatile__("BX      LR");
}
